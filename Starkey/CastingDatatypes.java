public class CastingDatatypes {

    public static void main(String[] args) {
        int i = 200;
        
        //to short
        short s = (short)i;
        System.out.println(s);

        //cast to double
        double d = (double)i;
        System.out.println(d);

        //cast to byte
        byte b = (byte)i;
        System.out.println(b);

        
    }
}